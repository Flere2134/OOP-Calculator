class Calculator:
#method for addition
    def add(self, num_1, num_2):
        return num_1 + num_2
#method for subtraction
    def subtract(self, num_1, num_2):
        return num_1 - num_2
#method for multiplication
    def multiply(self, num_1, num_2):
        return num_1 * num_2
#method for division
    def divide(self, num_1, num_2):
        return num_1 / num_2